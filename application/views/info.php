            <h2 class="blog-post-title">Tips Menjaga Kesehatan Agar Tetap Fit Saat Bekerja Shift</h2>
            <p class="blog-post-meta">December 1, 2021 by <a href="#">Mulyanto</a></p>

            <p>Proporsi pekerja shift semakin meningkat dari tahun ke tahun, ini disebabkan oleh investasi yang dikeluarkan untuk pengoperasian mesin-mesin yang mengharuskan penggunaannya secara terus menerus siang dan malam untuk memperoleh hasil yang lebih baik. Sebagai akibatnya pekerja juga harus bekerja siang dan malam.
Hal ini menimbulkan banyak masalah kesehatan terutama bagi tenaga kerja yang tidak atau kurang dapat menyesuaikan diri dengan jam kerja yang lazim.</p>
            
            <blockquote>
              <p>Menurut The Circadian Learning Centre di Amerika Serikat bahwa ketika ritme sirkadian menjadi tidak sinkron maka fungsi tubuh akan terganggu sehingga mudah mengalami gangguan tidur, kelelahan, penyakit jantung, tekanan darah tinggi, perubahan suhu tubuh, perubahan hormon, gangguan psikologi dan
gangguan gastrointestinal (Josling, 1999).</p>
            </blockquote>
            <p>Namun bagi Anda yang mendapat kerja dengan jadwal giliran/shifting, perlu siasat cerdas agar badan tetap fit dan terhindar dari penyakit.</p>
            <p>Agar anda tetap fit saat bekerja, anda bisa melakukan tips-tips berikut.</p>
			
            <ul>
              <li><strong>Tidur siang “bagi shift malam”</strong></li>
            </ul>
            <p>Sebelum bekerja di malam hari, biasakanlah untuk beristirahat yang cukup di siang hari agar anda bisa tetap aktif saat malam harinya. Tidur siang juga bisa mencegah infeksi akibat kelelahan pada mata.</p>
            <ul>
              <li><strong>Menjaga pola makan</strong></li>
            </ul>
			<p>Jagalah pola makan anda agar tetap teratur sepanjang hari, agar tubuh tetap fit sepanjang bekerja. Makanlah makanan yang mengandung serat, vitamin, dan protein. Dengan begitu, kesehatan tubuh anda tetap terjaga.</p>
			<ul>
              <li><strong>Minum air putih</strong></li>
            </ul>
			<p>Salah satu cara untuk menjaga tubuh tetap sehat adalah dengan minum air putih. Air putih dapat membantu untuk mengeluarkan racun dari tubuh anda, sehingga anda tetap sehat sampai malam hari.</p>
			<ul>
              <li><strong>Mengkonsumsi buah</strong></li>
            </ul>
			<p>Mengkonsumsi buah yang mengandung banyak air dan citrus bisa mengisi kekurangan cairan tubuh. Sebelum mulai bekerja, sebaiknya anda mengkonsumsi buah-buahan 3 jam sebelumnya.</p>
			<ul>
              <li><strong>Mengkonsumsi kacang-kacangan</strong></li>
            </ul>
			<p>Nutrisi kacang bisa meningkatkan energi. Oleh karena itu, kacang-kacangan sangat baik untuk dikonsumsi saat hendak memulai shift malam, sehingga anda akan tetap fit saat malam hari. Sebelum memulai bekerja, anda bisa mengkonsumsi kacang tanah atau kacang almond.</p>
